# Tipos de listas

lista1 = [1, 2, 3]
print("Lista 1:", lista1)

lista1.append(4)
print("Lista 1 (atualizada):", lista1)
#lista1.pop()
#print("Lista 1 (após pop):", lista1)
lista1.remove(2)
print("Lista 1 (após remove):", lista1)

# Indexação
print("Elemento na posição 0:", lista1[0])
print("Elemento na posição 1:", lista1[1])
print("Elemento na posição 2:", lista1[2])

# Contador
contador = 1
print(contador, lista1[0])
contador += 1
print(contador, lista1[1])
contador += 1
print(contador, lista1[2])


